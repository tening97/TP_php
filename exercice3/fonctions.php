<?php
$num1 = rand();
$num2 = rand();
function Somme($num1, $num2)
{
    $som = $num1 + $num2;
    return $som;
}
function Produit($num1, $num2)
{
    $prod = $num1 * $num2;
    return $prod;
}
function Difference($num1, $num2)
{
    $dif = $num1 - $num2;
    return $dif;
}
function Division($num1, $num2)
{
    $div = $num1 / $num2;
    return $div;
}
function Modulo($num1, $num2)
{
    $mod = $num1 % $num2;
    return $mod;
}
function Expo($num1, $num2)
{
    $exp = $num1 ** $num2;
    return $exp;
}
